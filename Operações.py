# Operadores (Aritmética)

n1 = 10
n2 = 20

print(n1 + n2)
print(n1 - n2)
print(n1 * n2)
print(n1 / n2)
print(n1 // n2) # Divisão inteira (descarta o decimal)
print(20 % 3) # Resto da divisão (mod)
print(2 ** 3) # 2 ao cubo

